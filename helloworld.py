print(3+2) #addition
print(3-2) #subtraction
print(3*2) #multiplication
print(3/2) #division
print(3//2) #floor division
print(3%2) #modulus
print(3**2) #exponentiation
print((3+2)*2) #using parentheses to change order of operations
print(3+2*2) #order of operations without parentheses
print(3**2*2) #order of operations with exponentiation

